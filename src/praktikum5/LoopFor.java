package praktikum5;

public class LoopFor {

    public static void main(String[] args) {
        //perulangan for
        int i;
        for (i = 0; i <= 10; i++) {
            System.out.println("Perulangan ke : " + i);
        }
        
        
        
        //perulangan while
        int j=0;
        
        while(j<10){
            System.out.println("Perulangan ke : " + j);
            j++;
        }
        
        //perulangan do while
        int k=0;  
        do{
            System.out.println("Perulangan ke : " + k);
            k++;
        } while(k<10);
        
        
    }

}
